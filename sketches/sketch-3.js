const container = document.getElementById("sketch3");
var w = container.clientWidth;
var h = container.clientHeight;

let sketch = function (p) {
  p.setup = function () {
    p.createCanvas(w, h);
    p.background(0, 0);
  };

  // Define a function to draw a single cell of the maze
  function drawCell(x, y) {
    if (grid[y][x] === 1) {
      p.fill(0, 0);
    } else {
      p.fill("#c7b198");
    }
    p.rect(x * (w / 25), y * (h / 25), w / 25, h / 25);
  }

  p.draw = function () {
    p.background(0, 0);
    p.stroke("#c7b198");

    // Iterate over each cell of the grid and draw it
    for (let y = 0; y < grid.length; y++) {
      for (let x = 0; x < grid[y].length; x++) {
        // Delay the drawing of each cell by 50 milliseconds
        setTimeout(() => drawCell(x, y), 50 * (y * grid[y].length + x));
      }
    }
  };
};
let myp5 = new p5(sketch, "sketch3");
